package Tema2;

public class RepasoArrays {
    public static void main(String[] args) {
        saludar();
        int resultado = sumar(5, 3);
        System.out.println("Suma: " + resultado);
    }

    // Ejercicio 1
    static void saludar() {
        System.out.println("Hola DAM1");
    }

    // Ejercicio 2
    static int sumar(int a, int b) {
        return a + b;
    }


}
